package CLASES;

import java.util.Date;

public class Pago {
    private Date fecha;
    private String metodo;
    private double monto;

    public Pago(Date fecha, String metodo, double monto) {
        this.fecha = fecha;
        this.metodo = metodo;
        this.monto = monto;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public String getMetodo() {
        return metodo;
    }

    public void setMetodo(String metodo) {
        this.metodo = metodo;
    }

    public double getMonto() {
        return monto;
    }

    public void setMonto(double monto) {
        this.monto = monto;
    }
    
    
}

